// day 86 — spherical harmonics initialization basics

import * as THREE from 'three';

// 1. The LightProbe
// The probe itself acts as a source of ambient lighting.
// You must remove any 'THREE.AmbientLight' or 'THREE.HemisphereLight' if you use a probe, 
// otherwise they will artificially stack up and blow out the scene.
const lightProbe = new THREE.LightProbe();
scene.add(lightProbe);

// 2. Spherical Harmonics Data (SH)
// SH is an array of 9 Vector3s representing color intensity 
// striking the probe from 360 geometric directions.
//
// These 9 vectors are usually read from an XML file generated by Unity/Unreal/Blender.
// We're initializing an empty structure here for demonstration.
const sh = new THREE.SphericalHarmonics3();

// 3. Modifying SH (Manual injection - not normally done by hand!)
// Vector index [0] corresponds roughly to overall ambient brightness (L0 band).
// Let's set the base ambient to a subtle red.
sh.coefficients[0].set(0.5, 0.1, 0.1);

// Vector index [1] relates to directional intensity (Y-axis - Up/Down).
// Let's make "up" slightly blue.
sh.coefficients[1].set(0.1, 0.1, 0.5);

// 4. Coping the data into the probe
lightProbe.sh.copy(sh);

// 5. Intensity Control
lightProbe.intensity = 1.0;

console.log('The light probe mathematically scatters the SH vector data over your meshes, calculating shading automatically.');
