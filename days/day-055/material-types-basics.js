/**
 * day 55 - material types
 */

/**
 * MeshBasicMaterial
 *
 * unlit material, no lighting calculations.
 * shows solid color or texture.
 * fast but unrealistic.
 */

/**
 * MeshStandardMaterial
 *
 * physically-based rendering (PBR).
 * realistic lighting and reflections.
 * industry standard for most 3D.
 */

/**
 * MeshPhongMaterial
 *
 * legacy specular lighting model.
 * cheaper than Standard.
 * less realistic highlights.
 */

/**
 * MeshLambertMaterial
 *
 * simplest lighting model.
 * diffuse only, no specular.
 * cheap but flat-looking.
 */

/**
 * other material types
 *
 * MeshMatcapMaterial: baked lighting
 * MeshToonMaterial: cel-shading
 * MeshNormalMaterial: debug normals
 * MeshDepthMaterial: depth visualization
 */
