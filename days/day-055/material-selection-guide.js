/**
 * material selection guide
 */

/**
 * when to use MeshBasicMaterial
 *
 * - UI elements and overlays
 * - unlit objects (glow effects)
 * - performance-critical scenes
 * - debugging
 */

/**
 * when to use MeshStandardMaterial
 *
 * - default choice for most objects
 * - realistic materials (metal, plastic, wood)
 * - environments with lighting
 * - PBR workflows
 */

/**
 * when to use MeshPhongMaterial
 *
 * - legacy projects
 * - mobile/low-end devices
 * - when you need cheaper specular
 */

/**
 * when to use MeshLambertMaterial
 *
 * - very low-end devices
 * - when only diffuse lighting is needed
 * - stylized/flat-shaded looks
 */

/**
 * special materials
 *
 * MeshToonMaterial: cartoon/cel-shaded
 * MeshMatcapMaterial: baked lighting (fast)
 * MeshNormalMaterial: debugging normals
 * MeshDepthMaterial: depth effects
 */
