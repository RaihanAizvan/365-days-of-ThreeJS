/**
 * MeshBasicMaterial use cases
 */

/**
 * UI and overlays
 *
 * perfect for UI elements that don't need lighting.
 * consistent appearance regardless of scene lighting.
 */

/**
 * performance-critical objects
 *
 * cheapest material - no lighting calculations.
 * use for many objects in performance-critical scenes.
 */

/**
 * emissive/glowing objects
 *
 * simulate glow without actual lighting.
 * bright colors appear to emit light.
 */

/**
 * debugging
 *
 * quickly visualize geometry without lighting setup.
 * wireframe mode for topology inspection.
 */

/**
 * stylized rendering
 *
 * flat-shaded art styles.
 * retro/low-poly aesthetics.
 */

/**
 * skyboxes and backgrounds
 *
 * backgrounds don't need lighting.
 * always visible regardless of scene lights.
 */
